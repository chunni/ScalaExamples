  val msg = "Hello " + name
  var i, j = 5
  val anonymous, unknown = "Anonym"
  println("msg = " + msg)
  println("i = " + i)
  println("j = " + j)
  println("anonymous = " + anonymous)
  println("unknown = " + unknown)
}
